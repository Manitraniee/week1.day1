public class PrimeNumbers {
	public static void main(String[] args) {
		int number = 13;
		// number % 2 -- remainder
		// condition -- true or false
		if(number%2 == 0) {
System.out.println(" 13 the number is even");
	}
	else if (number%2 != 0){
		System.out.println(" 13 the number is prime");
	}
}
}